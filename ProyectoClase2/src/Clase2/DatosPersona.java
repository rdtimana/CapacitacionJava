package Clase2;

/**
 * Clase para almacenar la información de una persona
 * @author josemauricio
 *
 */
public class DatosPersona {
	
	int identificacion;
	String tipoIdenticacion;
	String nombre_completo;
	
	/**
	 * @param identificacion
	 * @param tipoIdenticacion
	 * @param nombre_completo
	 */
	public DatosPersona(int identificacion, String tipoIdenticacion, String nombre_completo) {
		this.identificacion = identificacion;
		this.tipoIdenticacion = tipoIdenticacion;
		this.nombre_completo = nombre_completo;
	}
	
	
	
	
}
